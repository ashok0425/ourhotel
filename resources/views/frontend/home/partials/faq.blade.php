            <a href="">How do I find available hotels for the period I want to stay?</a>

            <p>
                In the search menu on the left containing the check-in and check-out data, please select the dates of
                your stay and tick ‘Show only available hotels’.
            </p>

            <a href="">
                How do I search for hotels by price?
            </a>
            <p>

                In the selection box at the top left of the search page you can sort the listing of hotels by price
                (when searching for available hotels on a specific date), popularity, class or name.

            </p>

            <a href="">
                Where can I find the directions to the hotel?
            </a>
            <p>

                At the bottom of the overview page of the hotel you can find travel information with details on location
                and directions. On the top left there is an extensive map of the area around the hotel.

            </p>

            <a href="">
                Can I generally request an extra bed for a third person in a double room and at what costs?
            </a>

            <p>
                It will depend on each hotel’s policy and availability. Once you have retrieved the availability for a
                specific hotel for the requested dates, by clicking on the room name, you will access the room
                description. If the possibility and the costs of an extra bed in the room are not specified in the room
                description, please send an e-mail to admin@nsnhotels.com
            </p>

            <a href="">
                Are the rates on your website per person or per room?
            </a>

            <p>
                All rates on the website are per room per stay, unless stated differently.
            </p>


            <a href="">
                Is breakfast included in the price?
            </a>

            <p>
                Once you have retrieved the availability for a specific hotel for the requested dates, by clicking on
                the room name, you will find information about breakfast, taxes and room facilities.

            </p>

            <a href="">
                Do you offer special discounts (for seniors, Corporate employees, etc)?
            </a>

            <p>
                We provide the best available rates for the dates of your stay. The discounts are already included and
